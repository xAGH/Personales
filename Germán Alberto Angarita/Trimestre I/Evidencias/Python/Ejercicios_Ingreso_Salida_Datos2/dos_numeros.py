"""Cree un programa que lea dos números y muestre
   su producto, su cociente, su módulo, su suma y 
   su resta.
"""
numero1 = int(input("Por favor digite el primer numero : "))
numero2 = int(input("Por favor digite el segundo numero : "))
producto = numero1 * numero2
cociente = numero1 / numero2
modulo = numero1 % numero2
suma = numero1 + numero2
resta = numero1 - numero2

print("El resultado de la multiplicación es : ",producto)
print("El resultado de la división es : ",cociente)
print("El residuo de la división es: ",modulo)
print("El resultado de la suma es : ",suma)
print("El resultado de la resta es : ",resta)












